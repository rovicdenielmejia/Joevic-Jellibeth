export const getTables = () => {
  const tableMap = new Map()
  
  const tableOrder = [
    "Parents' Table",
    "VIP Table 1",
    "VIP Table 2",
    ...Array.from({ length: 22 }, (_, i) => `Table #${i + 1}`),
  ]
  
  guests.forEach(guest => {
    if (!tableMap.has(guest.table)) {
      tableMap.set(guest.table, [])
    }
    tableMap.get(guest.table).push(guest)
  })
  
  return tableOrder.filter(table => tableMap.has(table)).map(table => ({
    name: table,
    guests: tableMap.get(table)
  }))
}

export const searchGuests = (query) => {
  if (!query || query.trim() === '') return []
  const searchTerm = query.toLowerCase().trim()
  return guests.filter(guest => guest.name.toLowerCase().includes(searchTerm))
}